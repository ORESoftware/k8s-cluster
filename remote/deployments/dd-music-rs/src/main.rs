fn title_for_seed(seed: u32) -> String {
    const LEFT: &[&str] = &[
        "Signal", "Violet", "Glass", "Metro", "Lattice", "Drift", "Amber", "Night", "Circuit",
        "River", "Static", "Pulse",
    ];
    const RIGHT: &[&str] = &[
        "Index", "Bloom", "Run", "Sketch", "Engine", "Room", "Field", "Trace", "Arc", "Thread",
        "Map", "Window",
    ];
    let left = LEFT[(seed as usize) % LEFT.len()];
    let right = RIGHT[((seed >> 8) as usize) % RIGHT.len()];
    format!("{left} {right}")
}

fn slug_for_title(title: &str, id: &str) -> String {
    let mut slug = String::new();
    for ch in title.chars() {
        if ch.is_ascii_alphanumeric() {
            slug.push(ch.to_ascii_lowercase());
        } else if !slug.ends_with('-') {
            slug.push('-');
        }
    }
    let slug = slug.trim_matches('-');
    let suffix = id.get(0..8).unwrap_or("song");
    format!("{slug}-{suffix}")
}

fn listenability_score(summary: &ArrangementSummary) -> f64 {
    let mut score: f64 = 0.0;
    if (0.025..=0.42).contains(&summary.rms) {
        score += 0.30;
    }
    if (0.35..=1.05).contains(&summary.peak) {
        score += 0.20;
    }
    if (120.0..=8_500.0).contains(&summary.spectral_centroid_hz) {
        score += 0.20;
    }
    if summary.rendered_events as f64 >= summary.duration_seconds.max(1.0) * 0.75 {
        score += 0.15;
    }
    if summary.drum_variation.variation_ratio()
        >= summary.drum_variation.repetition_reduction_target * 0.5
    {
        score += 0.15;
    }
    score.min(1.0)
}

