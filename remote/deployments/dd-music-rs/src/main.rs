async fn upsert_vote(
    client: &tokio_postgres::Client,
    song_id: &str,
    visitor_hash: &str,
    user_agent_hash: Option<&str>,
    vote_value: i32,
) -> Result<SongRow, ServiceError> {
    let sql = format!(
        "with upserted as (
            insert into {votes} (song_id, visitor_hash, user_agent_hash, vote_value)
            values ($1::uuid, $2, $3, $4)
            on conflict (song_id, visitor_hash)
            do update set vote_value = excluded.vote_value, updated_at = now()
            returning song_id
          ),
          stats as (
            select
              song_id,
              count(*) filter (where vote_value = 1)::int as up_votes,
              count(*) filter (where vote_value = -1)::int as down_votes,
              coalesce(sum(vote_value), 0)::int as vote_score
            from {votes}
            where song_id = $1::uuid
            group by song_id
          )
          update {songs} songs
             set up_votes = stats.up_votes,
                 down_votes = stats.down_votes,
                 vote_score = stats.vote_score,
                 updated_at = now()
            from stats
           where songs.id = stats.song_id
           returning
            songs.id::text as id,
            songs.title,
            songs.slug,
            songs.genre,
            songs.status,
            songs.audio_url,
            songs.content_type,
            songs.duration_millis,
            songs.bpm_millis,
            songs.listenability_score_micros,
            songs.vote_score,
            songs.up_votes,
            songs.down_votes,
            songs.play_count,
            songs.summary,
            to_char(songs.published_at at time zone 'utc', 'YYYY-MM-DD\"T\"HH24:MI:SS\"Z\"') as published_at,
            to_char(songs.created_at at time zone 'utc', 'YYYY-MM-DD\"T\"HH24:MI:SS\"Z\"') as created_at",
        songs = pg_contract::MUSIC_SONGS_TABLE,
        votes = pg_contract::MUSIC_SONG_VOTES_TABLE
    );
    let params: &[&(dyn ToSql + Sync)] = &[&song_id, &visitor_hash, &user_agent_hash, &vote_value];
    let row = client
        .query_opt(&sql, params)
        .await
        .map_err(db_error)?
        .ok_or_else(|| ServiceError::NotFound("song not found".to_string()))?;
    Ok(song_from_row(&row))
}

async fn generate_and_publish_songs(
    state: &AppState,
    count: i64,
    min_score: f64,
) -> Result<GenerateResponse, ServiceError> {
    let client = connect_postgres(&state.config).await?;
    let mut published = Vec::new();
    let mut discarded = 0usize;
    let mut attempts = 0usize;
    let max_attempts = (count * state.config.generation_max_attempts).max(count) as usize;

    while published.len() < count as usize && attempts < max_attempts {
        attempts += 1;
        let seed = seed_for_attempt(attempts as u64);
        let song = generate_candidate(seed, state.config.song_duration_seconds);
        if song.listenability_score < min_score {
            discarded += 1;
            insert_discarded_song(&client, &song, "listenability_score_below_threshold").await?;
            SONG_GENERATIONS.with_label_values(&["discarded"]).inc();
            continue;
        }
        let stored = store_audio(state, &song).await?;
        let row = insert_published_song(&client, &song, &stored).await?;
        published.push(row);
        SONG_GENERATIONS.with_label_values(&["published"]).inc();
    }

    Ok(GenerateResponse {
        ok: true,
        requested: count,
        published: published.len(),
        discarded,
        attempts,
        songs: published,
    })
}

fn generate_candidate(seed: u32, duration_seconds: f64) -> GeneratedSong {
    let id = Uuid::new_v4().to_string();
    let genre = genre_for_seed(seed);
    let title = title_for_seed(seed);
    let slug = slug_for_title(&title, &id);
    let bpm = (genre.default_bpm() + ((seed >> 5) % 17) as f64 - 8.0).clamp(62.0, 190.0);
    let structure_plan =
        generate_track_structure_plan(title.clone(), genre, duration_seconds, seed);
    let render = generate_microtonal_song(SongSpec {
        title: title.clone(),
        genre,
        duration_seconds,
        sample_rate: DEFAULT_SAMPLE_RATE,
        bpm,
        seed,
        palette: if seed % 2 == 0 {
            StylePalette::RaveCollage
        } else {
            StylePalette::BrokenBeatDream
        },
        structure_plan: Some(structure_plan),
        ..Default::default()
    });
    let listenability_score = listenability_score(&render.summary);
    let summary_json = summary_to_json(&render.summary);
    let audio_bytes = wav16_bytes(&render.audio);
    GeneratedSong {
        id,
        title,
        slug,
        seed: seed as i64,
        genre: genre.as_str().to_string(),
        audio_bytes,
        summary: render.summary,
        summary_json,
        listenability_score,
    }
}

async fn store_audio(state: &AppState, song: &GeneratedSong) -> Result<StoredObject, ServiceError> {
    match &state.config.storage {
        StorageConfig::S3(config) => {
            if config.bucket.is_empty() || config.public_base_url.is_empty() {
                return Err(ServiceError::Unavailable(
                    "S3 storage needs MUSIC_S3_BUCKET and MUSIC_S3_PUBLIC_BASE_URL".to_string(),
                ));
            }
            let Some(client) = &state.s3 else {
                return Err(ServiceError::Unavailable(
                    "S3 client is not configured".to_string(),
                ));
            };
            let key = format!(
                "{}/{}/{}.wav",
                config.key_prefix.trim_matches('/'),
                Utc::now().format("%Y-%m-%d"),
                song.slug
            );
            client
                .put_object()
                .bucket(&config.bucket)
                .key(&key)
                .content_type("audio/wav")
                .server_side_encryption(ServerSideEncryption::Aes256)
                .body(ByteStream::from(song.audio_bytes.clone()))
                .send()
                .await
                .map_err(|error| {
                    eprintln!("dd-music-rs S3 upload failed: {error}");
                    ServiceError::Unavailable("S3 upload failed".to_string())
                })?;
            Ok(StoredObject {
                provider: "s3".to_string(),
                bucket: Some(config.bucket.clone()),
                url: join_url(&config.public_base_url, &key),
                key,
            })
        }
        StorageConfig::Local(config) => {
            let date = Utc::now().format("%Y-%m-%d").to_string();
            let key = format!("{date}/{}.wav", song.slug);
            let path = std::path::Path::new(&config.root).join(&key);
            if let Some(parent) = path.parent() {
                tokio::fs::create_dir_all(parent).await.map_err(|error| {
                    eprintln!("dd-music-rs local storage mkdir failed: {error}");
                    ServiceError::Unavailable("local storage mkdir failed".to_string())
                })?;
            }
            tokio::fs::write(&path, &song.audio_bytes)
                .await
                .map_err(|error| {
                    eprintln!("dd-music-rs local storage write failed: {error}");
                    ServiceError::Unavailable("local storage write failed".to_string())
                })?;
            Ok(StoredObject {
                provider: "local".to_string(),
                bucket: None,
                url: join_url(&config.public_base_url, &key),
                key,
            })
        }
    }
}

