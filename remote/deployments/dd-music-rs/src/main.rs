async fn song_audio(
    State(state): State<AppState>,
    Path(song_id): Path<String>,
) -> Result<Redirect, ServiceError> {
    validate_song_id(&song_id)?;
    let client = connect_postgres(&state.config).await?;
    let sql = format!(
        "update {songs}
            set play_count = play_count + 1, updated_at = now()
          where id = $1::uuid and status = 'published'
          returning audio_url",
        songs = pg_contract::MUSIC_SONGS_TABLE
    );
    let row = client
        .query_opt(&sql, &[&song_id])
        .await
        .map_err(db_error)?
        .ok_or_else(|| ServiceError::NotFound("song not found".to_string()))?;
    let audio_url: Option<String> = row.get("audio_url");
    let Some(audio_url) = audio_url else {
        return Err(ServiceError::NotFound("song has no audio URL".to_string()));
    };
    if !is_allowed_audio_url(&state.config.storage, &audio_url) {
        eprintln!("dd-music-rs blocked untrusted audio URL for song {song_id}");
        return Err(ServiceError::NotFound(
            "song audio URL is not available".to_string(),
        ));
    }
    record_request("GET", "/songs/:song_id/audio", StatusCode::FOUND);
    Ok(Redirect::temporary(&audio_url))
}

async fn vote_song(
    State(state): State<AppState>,
    Path(song_id): Path<String>,
    Query(query): Query<VoteQuery>,
    headers: HeaderMap,
    body: Bytes,
) -> Result<Response, ServiceError> {
    validate_song_id(&song_id)?;
    let request = vote_request_from_parts(query, &headers, &body)?;
    if !matches!(request.value, -1 | 1) {
        return Err(ServiceError::BadRequest(
            "vote value must be 1 or -1".to_string(),
        ));
    }
    let visitor_hash = visitor_hash(&headers, &state.config.vote_hash_salt);
    throttle_vote(&state, &song_id, &visitor_hash).await?;
    let user_agent_hash = optional_header_hash(&headers, header::USER_AGENT.as_str());
    let client = connect_postgres(&state.config).await?;
    let song = upsert_vote(
        &client,
        &song_id,
        &visitor_hash,
        user_agent_hash.as_deref(),
        request.value,
    )
    .await?;
    SONG_VOTES
        .with_label_values(&[if request.value > 0 { "up" } else { "down" }])
        .inc();
    record_request("POST", "/songs/:song_id/votes", StatusCode::OK);
    if is_htmx_request(&headers) {
        Ok(Html(render_song_card(&song)).into_response())
    } else {
        Ok(Json(VoteResponse { ok: true, song }).into_response())
    }
}

async fn generate_internal(
    State(state): State<AppState>,
    headers: HeaderMap,
    Json(request): Json<GenerateRequest>,
) -> Result<Json<GenerateResponse>, ServiceError> {
    require_server_auth(&headers, &state.config)?;
    let requested = request.count.unwrap_or(1).clamp(1, 5);
    let min_score = request
        .min_score
        .unwrap_or(state.config.min_listenability_score)
        .clamp(0.0, 1.0);
    let result = generate_and_publish_songs(&state, requested, min_score).await?;
    record_request("POST", "/internal/generate", StatusCode::OK);
    Ok(Json(result))
}

