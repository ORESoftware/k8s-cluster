fn summary_number(summary: &Value, key: &str) -> f64 {
    summary.get(key).and_then(Value::as_f64).unwrap_or(0.0)
}

fn escape_html(value: &str) -> String {
    let mut escaped = String::with_capacity(value.len());
    for ch in value.chars() {
        match ch {
            '&' => escaped.push_str("&amp;"),
            '<' => escaped.push_str("&lt;"),
            '>' => escaped.push_str("&gt;"),
            '"' => escaped.push_str("&quot;"),
            '\'' => escaped.push_str("&#39;"),
            _ => escaped.push(ch),
        }
    }
    escaped
}

fn client_ip_for_hash(headers: &HeaderMap) -> String {
    if let Some(real_ip) = headers
        .get("X-Real-IP")
        .and_then(|value| value.to_str().ok())
        .map(str::trim)
        .filter(|value| !value.is_empty())
    {
        return real_ip.to_string();
    }
    headers
        .get("X-Forwarded-For")
        .and_then(|value| value.to_str().ok())
        .and_then(|value| value.split(',').next_back())
        .map(str::trim)
        .filter(|value| !value.is_empty())
        .unwrap_or("unknown")
        .to_string()
}

fn visitor_hash(headers: &HeaderMap, salt: &str) -> String {
    let forwarded = client_ip_for_hash(headers);
    let user_agent = headers
        .get(header::USER_AGENT)
        .and_then(|value| value.to_str().ok())
        .unwrap_or("unknown");
    hex_sha256(format!("{salt}\0{forwarded}\0{user_agent}").as_bytes())
}

fn optional_header_hash(headers: &HeaderMap, name: &str) -> Option<String> {
    headers
        .get(name)
        .and_then(|value| value.to_str().ok())
        .map(|value| hex_sha256(value.as_bytes()))
}

