fn first_env(keys: &[&str]) -> Option<String> {
    keys.iter()
        .find_map(|key| env::var(key).ok())
        .map(|value| value.trim().to_string())
        .filter(|value| !value.is_empty())
}

fn env_bool(name: &str, default: bool) -> bool {
    env::var(name)
        .ok()
        .map(|value| {
            matches!(
                value.trim().to_ascii_lowercase().as_str(),
                "1" | "true" | "yes" | "on"
            )
        })
        .unwrap_or(default)
}

fn env_i64(name: &str, default: i64) -> i64 {
    env::var(name)
        .ok()
        .and_then(|value| value.trim().parse::<i64>().ok())
        .filter(|value| *value > 0)
        .unwrap_or(default)
}

fn env_u64(name: &str, default: u64) -> u64 {
    env::var(name)
        .ok()
        .and_then(|value| value.trim().parse::<u64>().ok())
        .filter(|value| *value > 0)
        .unwrap_or(default)
}

fn env_f64(name: &str, default: f64) -> f64 {
    env::var(name)
        .ok()
        .and_then(|value| value.trim().parse::<f64>().ok())
        .filter(|value| value.is_finite() && *value > 0.0)
        .unwrap_or(default)
}

fn config_from_env() -> Config {
    let storage_provider = first_env(&["MUSIC_STORAGE_PROVIDER", "DEFAULT_STORAGE_PROVIDER"])
        .unwrap_or_else(|| "s3".to_string())
        .to_ascii_lowercase();
    let storage = if storage_provider == "local" {
        StorageConfig::Local(LocalStorageConfig {
            root: first_env(&["MUSIC_LOCAL_STORAGE_ROOT", "LOCAL_STORAGE_ROOT"])
                .unwrap_or_else(|| "/tmp/dd-music-rs/audio".to_string()),
            public_base_url: first_env(&[
                "MUSIC_LOCAL_PUBLIC_BASE_URL",
                "LOCAL_STORAGE_PUBLIC_BASE_URL",
            ])
            .unwrap_or_else(|| "/local-audio".to_string()),
        })
    } else {
        StorageConfig::S3(S3StorageConfig {
            bucket: first_env(&["MUSIC_S3_BUCKET", "S3_BUCKET"]).unwrap_or_default(),
            public_base_url: first_env(&["MUSIC_S3_PUBLIC_BASE_URL", "S3_PUBLIC_BASE_URL"])
                .unwrap_or_default(),
            key_prefix: first_env(&["MUSIC_S3_KEY_PREFIX", "S3_KEY_PREFIX"])
                .unwrap_or_else(|| "music/generated".to_string()),
        })
    };

    let daily_target_min = env_i64("MUSIC_DAILY_TARGET_MIN", DEFAULT_DAILY_TARGET_MIN);
    let daily_target_max =
        env_i64("MUSIC_DAILY_TARGET_MAX", DEFAULT_DAILY_TARGET_MAX).max(daily_target_min);
    let vote_hash_salt = first_env(&["MUSIC_VOTE_HASH_SALT", "SERVER_AUTH_SECRET"]);
    let vote_hash_salt_configured = vote_hash_salt.is_some();
    let vote_hash_salt =
        vote_hash_salt.unwrap_or_else(|| format!("dd-music-rs-local-{}", Uuid::new_v4()));

    Config {
        database_url: first_env(&[
            "MUSIC_RDS_DATABASE_URL",
            "AGENT_TASKS_RDS_DATABASE_URL",
            "RDS_DATABASE_URL",
            "DATABASE_URL",
            "PG_DATABASE_URL",
        ]),
        server_auth_secret: first_env(&["MUSIC_SERVER_AUTH_SECRET", "SERVER_AUTH_SECRET"]),
        allow_unauthenticated_internal: env_bool("MUSIC_ALLOW_UNAUTHENTICATED_INTERNAL", false),
        generator_enabled: env_bool("MUSIC_GENERATOR_ENABLED", false),
        generator_interval: Duration::from_secs(env_u64(
            "MUSIC_GENERATOR_INTERVAL_SECONDS",
            DEFAULT_GENERATOR_INTERVAL_SECONDS,
        )),
        generator_initial_delay: Duration::from_secs(env_u64(
            "MUSIC_GENERATOR_INITIAL_DELAY_SECONDS",
            20,
        )),
        generator_max_per_sweep: env_i64(
            "MUSIC_GENERATOR_MAX_PER_SWEEP",
            DEFAULT_GENERATOR_MAX_PER_SWEEP,
        ),
        generation_max_attempts: env_i64(
            "MUSIC_GENERATION_MAX_ATTEMPTS",
            DEFAULT_GENERATION_MAX_ATTEMPTS,
        ),
        daily_target_min,
        daily_target_max,
        song_duration_seconds: env_f64(
            "MUSIC_SONG_DURATION_SECONDS",
            DEFAULT_SONG_DURATION_SECONDS,
        )
        .clamp(MIN_SONG_DURATION_SECONDS, MAX_SONG_DURATION_SECONDS),
        min_listenability_score: env_f64(
            "MUSIC_MIN_LISTENABILITY_SCORE",
            DEFAULT_MIN_LISTENABILITY_SCORE,
        )
        .min(1.0),
        redis_prefix: first_env(&["MUSIC_REDIS_PREFIX"]).unwrap_or_else(|| {
            dd_redis_interfaces::MUSIC_DAILY_GENERATION_TARGET_KEY_DEFAULT_PREFIX.to_string()
        }),
        vote_hash_salt,
        vote_hash_salt_configured,
        vote_throttle_seconds: env_u64(
            "MUSIC_VOTE_THROTTLE_SECONDS",
            DEFAULT_VOTE_THROTTLE_SECONDS,
        ),
        storage,
    }
}

async fn state_from_config(config: Config) -> Result<AppState, String> {
    let redis = first_env(&["MUSIC_REDIS_URL", "REDIS_URL"])
        .or_else(|| Some(DEFAULT_REDIS_URL.to_string()))
        .and_then(|url| redis::Client::open(url).ok());

    let s3 = match &config.storage {
        StorageConfig::S3(s3_config) if !s3_config.bucket.is_empty() => {
            let region = first_env(&[
                "MUSIC_S3_REGION",
                "S3_REGION",
                "AWS_REGION",
                "AWS_DEFAULT_REGION",
            ]);
            let region_provider = RegionProviderChain::first_try(region.map(Region::new))
                .or_default_provider()
                .or_else("us-east-1");
            let shared_config = aws_config::defaults(aws_config::BehaviorVersion::latest())
                .region(region_provider)
                .load()
                .await;
            let mut builder = aws_sdk_s3::config::Builder::from(&shared_config);
            if let Some(endpoint) = first_env(&["MUSIC_S3_ENDPOINT", "S3_ENDPOINT"]) {
                builder = builder.endpoint_url(endpoint);
            }
            Some(aws_sdk_s3::Client::from_conf(builder.build()))
        }
        _ => None,
    };

    let nats = match first_env(&["MUSIC_NATS_URL", "NATS_URL"]) {
        Some(url) => match async_nats::connect(&url).await {
            Ok(client) => Some(client),
            Err(error) => {
                eprintln!("dd-music-rs NATS connect failed ({url}): {error}");
                None
            }
        },
        None => None,
    };

    Ok(AppState {
        config: Arc::new(config),
        redis,
        redis_connection: Arc::new(Mutex::new(None)),
        s3,
        nats,
    })
}

impl AppState {
    async fn redis_connection(&self) -> Result<redis::aio::MultiplexedConnection, ServiceError> {
        let Some(client) = &self.redis else {
            return Err(ServiceError::Unavailable(
                "redis is not configured".to_string(),
            ));
        };
        let mut guard = self.redis_connection.lock().await;
        if let Some(connection) = guard.as_ref() {
            return Ok(connection.clone());
        }
        let connection = client
            .get_multiplexed_async_connection()
            .await
            .map_err(|error| {
                eprintln!("dd-music-rs redis connection failed: {error}");
                ServiceError::Unavailable("redis connection failed".to_string())
            })?;
        *guard = Some(connection.clone());
        Ok(connection)
    }
}

fn record_request(method: &str, path: &str, status: StatusCode) {
    HTTP_REQUESTS
        .with_label_values(&[method, path, status.as_str()])
        .inc();
}

fn now_ms() -> u128 {
    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap_or_default()
        .as_millis()
}

async fn connect_postgres(config: &Config) -> Result<tokio_postgres::Client, ServiceError> {
    let database_url = config
        .database_url
        .as_deref()
        .ok_or_else(|| ServiceError::Unavailable("music service is not ready".to_string()))?;
    let mut root_store = rustls::RootCertStore::empty();
    root_store.extend(webpki_roots::TLS_SERVER_ROOTS.iter().cloned());
    let tls_config = rustls::ClientConfig::builder()
        .with_root_certificates(root_store)
        .with_no_client_auth();
    let tls = tokio_postgres_rustls::MakeRustlsConnect::new(tls_config);
    let (client, connection) =
        tokio_postgres::connect(database_url, tls)
            .await
            .map_err(|error| {
                eprintln!("dd-music-rs postgres connect failed: {error}");
                ServiceError::Unavailable("postgres connect failed".to_string())
            })?;
    tokio::spawn(async move {
        if let Err(error) = connection.await {
            eprintln!("dd-music-rs postgres connection error: {error}");
        }
    });
    Ok(client)
}

fn require_server_auth(headers: &HeaderMap, config: &Config) -> Result<(), ServiceError> {
    if config.allow_unauthenticated_internal {
        return Ok(());
    }
    let expected = config
        .server_auth_secret
        .as_deref()
        .ok_or(ServiceError::Unauthorized)?;
    let presented = headers
        .get("X-Server-Auth")
        .or_else(|| headers.get("Auth"))
        .and_then(|value| value.to_str().ok())
        .unwrap_or("");
    if constant_time_eq(presented, expected) {
        Ok(())
    } else {
        Err(ServiceError::Unauthorized)
    }
}

